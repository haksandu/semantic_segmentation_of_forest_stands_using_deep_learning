# Settings ----------------------------------------------------------------

# Set correct path
folder_path <- "./data/"

# Select grid size
gr <- "512_grid.gpkg"



# -------------------------------------------------------------------------

# Load packages
library(terra)



###########################################################################
#################            RGB            ###############################
###########################################################################


# Get grid ----------------------------------------------------------------
grid <- vect(paste0(folder_path, gr))


# VRT ---------------------------------------------------------------------

# load VRT 
rgbVRT <- vrt("./data/RGB_mev.vrt")


# Make tiles
setwd("./data/img_tiles/rgb")
a <- makeTiles(rgbVRT, grid)


rm(a, rgbVRT)


###########################################################################
#################            CIR            ###############################
###########################################################################


# Get grid ----------------------------------------------------------------

